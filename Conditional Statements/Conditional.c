// Main Library
#include <stdio.h>

// Main function
int main() {
    // Var int
    int num = 10;

    // Conditional Statement
    if (num = 10) {
        printf("You guessed the right num! %d", num);
    } else {
        printf("You guessed the wrong num.");
    }

    // Return 0
    return 0;
}